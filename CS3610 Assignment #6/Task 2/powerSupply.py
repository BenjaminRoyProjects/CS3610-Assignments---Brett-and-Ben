class powerSupply():
    def applyPower():
        print("Powering Computer")
        
    def powerVideoCard():
        print("Powering the Video Card")
        
    def powerRAM():
        print("Powering the RAM")
        
    def powerOpticalDiscReader():
        print("Powering the Disc Reader")
    
    def powerHDD():
        print("Powering the Hard Drive")
    
    def stopPowerVideoCard():
        print("Stopping power to the Video Card")
        
    def stopPowerRam():
        print("Stopping power to the RAM")
    
    def stopPowerOpticalDiscReader():
        print("Stopping power to the Disc Reader")
    
    def stopPowerHDD():
        print("Stopping power to the HDD")
        
    def stopPower():
        print("Stopping power to Computer")
    